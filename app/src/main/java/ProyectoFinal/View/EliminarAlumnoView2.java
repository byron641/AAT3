 private void cargarAlumnos() {
    List<ModificarAlumnoModel> alumnos = controlador.leerAlumnos();
    
    System.out.println("Cantidad de alumnos: " + alumnos.size());
    
    JcbAlumno.removeAllItems();
    
    for (ModificarAlumnoModel alumno : alumnos) {
        JcbAlumno.addItem(alumno.getClaveAlumno() + " - " + alumno.getNombreAlumno() + " " + alumno.getApellidoAlumno());
    }
}
 
 private void cargarDatosAlumno(int clave) {
        ModificarAlumnoModel alumno = controlador.cargarDatosAlumno(clave);
        if (alumno != null) {
            TxfModificarAlumno.setText(alumno.getNombreAlumno());
            TxfModificarApellido.setText(alumno.getApellidoAlumno());
            TxfModificarClave.setText(String.valueOf(alumno.getClaveAlumno()));
        } else {
            limpiarCampos();
            JOptionPane.showMessageDialog(null, "Alumno no encontrado", "Error", JOptionPane.ERROR_MESSAGE);
        }
 }

    private void limpiarCampos() {
        TxfModificarAlumno.setText("");
        TxfModificarApellido.setText("");
        TxfModificarClave.setText("");
    }

private void eliminarAlumno(int clave) {
    ModificarAlumnoModel modelo = new ModificarAlumnoModel(); 
    String mensaje = modelo.eliminarAlumno(clave); 
    JOptionPane.showMessageDialog(null, mensaje, "Informaci√≥n", JOptionPane.INFORMATION_MESSAGE);
    cargarAlumnos();
    limpiarCampos();
}
